export const get_admin = async () => {
    try {
        const admin = await UserModel.findOne({ is_admin: true });
        return admin;
    } catch (err) {
        return null;
    }
}

export const sendAdminNotification = async (ctx, users) => {
    try {
        const admin = await get_admin();
        if (!admin) return null;
        api.sendMessage(admin.id, `<b>✅ New user registered\n👤 Mention: ${user_mention(ctx.from)}\n🆔 UserID: <code>${ctx.from.id}</code>\n👑 Pos: <code>${users}</code></b>`, options);
        return true;
    } catch (err) {
        return null;
    }
}

