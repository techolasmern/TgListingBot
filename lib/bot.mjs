export const sendBroadcast = async (id, message_id_to_edit, message_id, callback) => {
    const admin = await get_admin();
    if (!admin || admin.id !== id) {
        return callback("❌ Access Denied: Admin Privileges Required");
    }
    await api.editMessageText(id, message_id_to_edit, `<b>✅ Broadcast in Progress</b>\n\nPlease wait while the broadcast is in progress.\n\n<b>Message ID:</b> <code>${message_id}</code>`, options);
    const obj = {
        message_id,
        sent: 0,
        success: 0,
        failed: 0
    }
    const bTask = nodeCron.schedule("*/3 * * * * *", async () => {
        const user = await UserModel.findOne({ broadcasted: { $ne: true } });
        if (!user) {
            bTask.stop();
            return callback(`<b>✅ Message Sent\n\nMessage has been sent successfully.\n\nMessage ID: <code>${obj.message_id}</code>\n\nSent: <code>${obj.sent}</code>\nSuccess: <code>${obj.success}</code>\nFailed: <code>${obj.failed}</code></b>`);
        }
        try {
            await api.copyMessage(user.id, id, obj.message_id);
            obj.success++;
        } catch (err) {
            obj.failed++;
        } finally {
            user.broadcasted = true;
            obj.sent++
        }
        await user.save();
    });
}